def get_initial_features():
    vec0 = ["total_accel", "var_accel"]
    nn1 = [
        "avg",
        "stddev",
        "var",
        "kurtosis",
        "skewness",
        "min",
        "max",
        "amplitude",
    ]
    vec1 = (
        ["roll"]
        + [f"{s}_roll" for s in nn1]
        + ["pitch"]
        + [f"{s}_pitch" for s in nn1]
        + ["yaw"]
        + [f"{s}_pitch" for s in nn1]
    )
    nn2 = ["gyros", "accel", "magnet"]
    vec2 = [f"{a}_VVV_{b}" for a in nn2 for b in ["x", "y", "z"]]
    vec_vvv = [f"{x}_VVV" for x in vec0 + vec1] + vec2
    vec_belt = [x.replace("_VVV", "_belt") for x in vec_vvv]
    vec_arm = [x.replace("_VVV", "_arm") for x in vec_vvv]
    vec_forearm = [x.replace("_VVV", "_forearm") for x in vec_vvv]
    vec_dumbbell = [x.replace("_VVV", "_dumbbell") for x in vec_vvv]
    return vec_belt + vec_arm + vec_forearm + vec_dumbbell + ["classe"]


